Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na úrovni jednotlivých nukleotidů, takže může detekovat "Stealth Omicron", které jsou obtížně detekovatelné PCR testy.
V současné době Korea Centers for Disease Control and Prevention používá tři metody pro detekci variant COVID-19: analýzu celého genomu, analýzu cílové DNA (mutace, jako je například spike protein) a PCR test.
