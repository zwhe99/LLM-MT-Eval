Molekulární diagnostický test dokáže detekovat variantu Omikronu za 20 minut: Zpráva
Korejští výzkumníci vyvinuli molekulární diagnostickou technologii, která dokáže detekovat varianty Omikronu.
Vývoj technologií je v současnosti dokončen a předpokládá se, že bude nějaký čas trvat, než se technologie dostane na trh.
POSTECH oznámil 10. prosince, že výzkumný tým vedený profesorem Lee Jung-wook z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicron za pouhých 20-30 minut a výsledky publikuje online.
Omicron je varianta, která obsahuje 26-32 mutací v křivce, která slouží k infekci buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na jediném nukleotidovém základu, takže může detekovat "Stealth Omicron", který je obtížně detekovatelný pomocí PCR testů.
Aktuálně Korejské centrum pro kontrolu a prevenci nemocí používá tři metody k detekci variant COVID-19: analýza celého genomu, analýza cílového DNA (mutace, jako je špicový protein) a PCR test.
V případě Delta varianty ji lze detekovat současným PCR testem, avšak u varianty Omicron to není možné.
Nově vyvinutá technologie tentokrát není sekvenační metoda, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Existující technologie zobrazuje pouze konkrétní oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobila reakce vázání nukleových kyselin pouze v přítomnosti COVID-19 RNA, což umožňuje rychlé zjištění nákazy.
Podle profesora Lee má Omikron silný signál pro N geny v PCR testech, ale slabý signál pro S geny.
V případě "Stealth Omikronu" byly potvrzeny oba geny N a S jako pozitivní, což ztěžuje odlišení od jiných variant.
Molekulární diagnostická technologie pracuje s rozdílnými mechanismy než PCR a účinně detekuje Omikronskou variantu.
Na rozdíl od konvenční technologie, která obvykle zpracovává až 96 vzorků na zařízení, nová technologie může zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje specializované vybavení, což umožňuje snadné a jednoduché vytváření diagnostických sestav.
Díky tomuto postupu lze vyvinout diagnostický kit během 4 dní, což v případě výskytu nové varianty nebo viru v budoucnosti předpokládá rychlou reakci.
"Doufám, že zveřejnění této technologie nám pomůže co nejdříve se vrátit do běžného každodenního života," řekl profesor Lee.
