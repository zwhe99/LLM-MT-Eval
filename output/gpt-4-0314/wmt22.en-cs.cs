Molekulární diagnostický test dokáže zjistit variantu Omicron za 20 minut: zpráva
Korejští vědci vyvinuli molekulárně diagnostickou technologii, která dokáže detekovat varianty Omicron.
Vývoj technologie byl v současné době dokončen a očekává se, že komercializace potrvá určitý čas.
POSTECH oznámila 10. dne, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulárně diagnostickou technologii, která dokáže detekovat variantu Omicron během pouhých 20-30 minut a výsledky budou zveřejněny online.
Omicron je varianta, ve které se nachází 26-32 mutací v bodečku, který COVID-19 virus používá k infikování buněk.
Podle výzkumného týmu dokáže technologie molekulární diagnostiky rozlišit mutace na úrovni jednotlivých nukleotidových bází, takže dokáže detekovat "Stealth Omikron", který je obtížně zjistitelný pomocí PCR testů.
Aktuálně Korea Centers for Disease Control and Prevention používá tři metody pro detekci variant COVID-19: analýzu celého genomu, analýzu cílové DNA (mutace, jako je například spike protein) a PCR test.
V případě varianty Delta ji lze detekovat současným PCR testem, ale Omicron ne.
Nově vyvinutá technologie tentokrát není sekvenční metodou, která čte DNA nebo RNA sekvence, ale molekulárně diagnostickou technologií.
Stávající technologie prochází pouze konkrétní oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobila vazbu nukleových kyselin pouze v přítomnosti RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má Omicron silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě "Stealth Omicron" byly potvrzeny pozitivní jak gen N, tak gen S, což ztěžuje rozlišení od ostatních variant.
Molekulární diagnostická technologie pracuje na odlišných mechanismech od PCR, efektivně detekující variantu Omikron.
Oproti běžné technologii, která zpravidla zpracovává až 96 vzorků na zařízení, dokáže nová technologie zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Navíc tato technologie nepotřebuje specializované vybavení, takže diagnostické sady mohou být jednoduše a snadno vytvořeny.
Jelikož metoda dokáže vyvinout diagnostickou sadu za 4 dny, očekává se, že rychle zareaguje i v případě, že se v budoucnosti objeví nová varianta nebo virus.
"Doufám, že zveřejnění této technologie nám pomůže co nejdříve se vrátit k normálnímu každodennímu životu," řekl profesor Lee.
Pokud se objeví nové varianty po COVID-19, pokusíme se je rychle diagnostikovat a reagovat na ně.
Tato technologie je v současnosti před komercializací.
Nicméně, lze jej použít jako pomocný nástroj v aktuálních situacích, kde nebyl vyvinut PCR test pro Omikron.
Profesor Lee řekl: "Myslím si, že tato technologie bude blízko komercializace ve druhé polovině příštího roku po klinických testech."
Důvod, proč zveřejňuji tuto technologii, je sdílet ji s ostatními, abychom společně vyvinuli lepší technologie pro překonání COVID-19 a umožnili i méně rozvinutým zemím analyzovat varianty COVID-19."
